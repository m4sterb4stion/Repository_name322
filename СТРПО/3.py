def print_odd_numbers(A, B):
    """
    Выводит все нечетные числа от A до B включительно в порядке убывания.
    A > B
    """
    # Проверяем, чтобы A было больше B
    if A <= B:
        print("Ошибка: A должно быть больше B")
        return
    
    print(f"Нечетные числа от {A} до {B} в порядке убывания:")
    
    # Перебираем числа от A до B включительно в порядке убывания
    for num in range(A, B - 1, -1):  # Шаг -1 для убывания
        # Проверяем, является ли число нечетным
        if num % 2 != 0:
            print(num, end=" ")
    print()  # Переход на новую строку после вывода


# Пример использования функции
if __name__ == "__main__":
    # Ввод чисел A и B
    A = int(input("Введите число A: "))
    B = int(input("Введите число B (должно быть меньше A): "))
    
    # Вызов функции
    print_odd_numbers(A, B)